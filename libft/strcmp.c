int	ft_strcmp(const char *s1, const char *s2)
{
	if (*s1 && *s2)
		return (*s1 == *s2 ? ft_strcmp(s1 + 1, s2 + 1) : (*s1 - *s2));
	return (*s1 - *s2);
}
