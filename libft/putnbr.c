#include "libft.h"

void	ft_putnbr(int n)
{
	long	l;

	l = (long)n;
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 10 || n < -10)
		ft_putnbr(n / 10);
	ft_putchar('0' + (n % 10));
}
